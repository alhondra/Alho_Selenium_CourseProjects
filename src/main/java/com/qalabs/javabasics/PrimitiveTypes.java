package com.qalabs.javabasics;

public class PrimitiveTypes {
    public static void main (String[] args) {
        // 8 bit signed, min -128, max 127, default 0
        byte myByte = 10;
        // 16 bit signed, min -32768, max 32767, default 0
        short myShort = 500;
        //32 bit signed, min (-2^31) , max (2^31 -1), default 0
        int myInt = 100000;
        // 64 bit signed, min (-2^63) , max (2^63 -1), default 0
        long myLong = 10000000;
        //single precision 32 bits floating point
        float myFloat = 3.2f;
        //double date precision 65 bits floating point
        double myDouble = 3.5d;
        //1 bit, only two possible values true or false
        boolean myBoolean = true;
        //16 bit unicode character
        char myChar = 'A';
    }
}
